public class Pessoa {
    private char nome;
    private char cpf;
    private char dataNascimento;
    private char endereco;
    private char email;
    private char telefone;


    public char getNome() {
        return nome;
    }

    public void setNome(char nome) {
        this.nome = nome;
    }

    public char getCpf() {
        return cpf;
    }

    public void setCpf(char cpf) {
        this.cpf = cpf;
    }

    public char getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(char dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public char getEndereco() {
        return endereco;
    }

    public void setEndereco(char endereco) {
        this.endereco = endereco;
    }

    public char getEmail() {
        return email;
    }

    public void setEmail(char email) {
        this.email = email;
    }

    public char getTelefone() {
        return telefone;
    }

    public void setTelefone(char telefone) {
        this.telefone = telefone;
    }
}
