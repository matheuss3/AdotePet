public class Animal{
	private char raca, nome;
	private int idade;
	
	public char getNome(){
    return nome;
  }
	
	public void setNome(char nome){
    this.nome=nome;
  }

	public char getRaca(){
    return raca;
  }
	
	public void setRaca(char raca){
    this.raca=raca;
  }

	public int getIdade(){
    return idade;
  }
	
	public void setIdade(int idade){
    this.idade=idade;
  }
}